    private void btn_rechazarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_rechazarActionPerformed
        int YesOrNo = JOptionPane.showConfirmDialog(null,"Está seguro que desea rechazar este espectáculo?","Rechazar espectáculo", JOptionPane.YES_NO_OPTION);
        if(YesOrNo == 0)
        {
            ICE.aceptarEspectaculos(this.espectaculos.getSelectedItem().toString(), false);
            espectaculos.removeAllItems();
            Map<String, Espectaculo> espectaculosIngresados = ICE.getMapEspectaculoIngresados();
            espectaculosIngresados.entrySet().forEach((entry) -> {
                espectaculos.addItem(entry.getValue().getNombre());
            });
        }
        
    }//GEN-LAST:event_btn_rechazarActionPerformed

    private void btn_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aceptarActionPerformed
        /* Cuando se le da click a  */
        int YesOrNo = JOptionPane.showConfirmDialog(null,"Está seguro que desea aceptar este espectáculo?","Aceptar espectáculo", JOptionPane.YES_NO_OPTION);
        if(YesOrNo == 0)
        {
            ICE.aceptarEspectaculos(this.espectaculos.getSelectedItem().toString(), true);
            espectaculos.removeAllItems();
            Map<String, Espectaculo> espectaculosIngresados = ICE.getMapEspectaculoIngresados();
            espectaculosIngresados.entrySet().forEach((entry) -> {
                espectaculos.addItem(entry.getValue().getNombre());
            });
        }
        
    }//GEN-LAST:event_btn_aceptarActionPerformed


